// Helper to generate IDs sequentially
export const QUIZ_DATA: Question[] = RAW_DATA.map((item, index) => {
  const [nameWithEn, code, capital, climate, culture, flagDesc, d1, d2, d3] = item;
  
  // Clean the country name (Remove English part)
  const nameZh = nameWithEn.split(' (')[0];

  // Construct a natural question string
  const questionText = `哪個氣候為${climate}氣候的國家，國旗${flagDesc}，且以${culture}著稱？`;

  // Check if country is East Asian based on code
  const isEastAsia = EAST_ASIA_CODES.includes(code);

  // Create options array with pure Chinese names
  const options = [
    { id: 'A', label: 'A', text: nameZh, isCorrect: true },
    { id: 'B', label: 'B', text: d1, isCorrect: false },
    { id: 'C', label: 'C', text: d2, isCorrect: false },
    { id: 'D', label: 'D', text: d3, isCorrect: false },
  ];

  // Shuffle options deterministically based on index to avoid "A" always being correct
  const shuffledOptions = [...options];
  const shift = index % 4; // 0, 1, 2, 3
  
  // Rotate the array
  for (let i = 0; i < shift; i++) {
    shuffledOptions.push(shuffledOptions.shift()!);
  }

  // Re-assign IDs and Labels A, B, C, D after shuffle
  const finalOptions = shuffledOptions.map((opt, i) => ({
    ...opt,
    id: ['A', 'B', 'C', 'D'][i],
    label: ['A', 'B', 'C', 'D'][i]
  }));

  return {
    id: index + 1,
    country: nameZh,
    capital: capital,
    flagCode: code,
    flagUrl: `https://flagcdn.com/w640/${code}.png`,
    questionText,
    isCapitalQuestion: false, 
    options: finalOptions,
    region: isEastAsia ? 'EastAsia' : 'Other'
  };
});